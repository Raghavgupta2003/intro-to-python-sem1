# Demonstrating NOT operator
print("not 'Hello'  →", not "Hello")   # non-empty string = True → not True = False
print("not 5        →", not 5)         # non-zero = True → False
print("not 0        →", not 0)         # zero = False → True

print()  # just for spacing

# Comparison operator ==
a = 2
b = 2
print("a == b       →", a == b)        # True
print("2 == 2       →", 2 == 2)        # True

print()  # spacing

# Logical OR operator
print("True or True   →", True or True)
print("True or False  →", True or False)
print("False or True  →", False or True)

print()  # spacing

# Logical AND operator
print("True and False →", True and False)
